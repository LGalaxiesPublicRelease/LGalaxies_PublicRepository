#include <math.h>
#include "manglefn.h"

/*------------------------------------------------------------------------------
  Round x to n decimal places.
*/
double places(double x, int n)
{
    int i;

    for (i = 0; i < n; i++) x = x * 10.;
    x = rint(x);
    for (i = 0; i < n; i++) x = x / 10.;

    return(x);
}
